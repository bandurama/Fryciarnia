CREATE TABLE DbRecipe (
    uuid NVARCHAR2(128) NOT NULL,
    meal NVARCHAR2(128) NOT NULL,
    ingridient NVARCHAR2(128) NOT NULL,
    quantity NUMERIC(10,2),
    instruction NVARCHAR2(64),
